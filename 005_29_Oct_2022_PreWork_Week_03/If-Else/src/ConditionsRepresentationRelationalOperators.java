
public class ConditionsRepresentationRelationalOperators {

	public static void main(String[] args) {
		
		boolean condition = (30 < 80);
		
		if (condition) {
			System.out.println("Value of condition " + condition);
		}
		
		condition = 80 > 30;
		if (condition) {
			System.out.println("Value of condition " + condition);
		}
		
		condition = (3 != 10);
		if (condition) {
			System.out.println("Value of condition " + condition);
		}
		
	}
}
